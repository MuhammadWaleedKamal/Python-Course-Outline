# IF-IF Statement examples (independent conditions)

number = 10
if number > 5:
    print("Number is greater than 5")
if number % 2 == 0:
    print("Number is even")

age = 20
if age >= 18:
    print("Adult")
if age >= 20:
    print("Eligible for a senior student discount")

