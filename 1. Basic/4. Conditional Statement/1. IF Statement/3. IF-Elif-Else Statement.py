# IF-Elif-Else Statement examples

score = 92
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: D or below")

temperature = 18
if temperature > 30:
    print("It is hot outside.")
elif temperature >= 20:
    print("It is warm outside.")
else:
    print("It is cold outside.")

