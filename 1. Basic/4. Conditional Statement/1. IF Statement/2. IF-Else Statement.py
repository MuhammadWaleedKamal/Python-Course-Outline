# IF-Else Statement examples

number = 10
if number > 10:
    print("Number is greater than 10")
else:
    print("Number is not greater than 10")

age = 17
if age >= 18:
    print("You are allowed to vote.")
else:
    print("You are not allowed to vote yet.")

